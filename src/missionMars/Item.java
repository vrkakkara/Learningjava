package missionMars;

public class Item {
	private String itemName;
	private double itemWeight;
	public Item(String itemName, double itemWeight) {
		super();
		this.itemName = itemName;
		this.itemWeight = itemWeight;
	}
	public String getItemName() {
		return itemName;
	}
	public double getItemWeight() {
		return itemWeight;
	}
	

}
